#include <iostream>
using namespace std;

int main() {
	cout << "!!!Hello World!!!" << endl; // prints !!!Hello World!!!
	/*
	 * cout cout: does not show data in the terminal.
	 * "<<" is the assignment operator. Transfer the data on the right to the left.
	 * endl: this is used to switch to a sub-line.
	 */
	return 0;
}
